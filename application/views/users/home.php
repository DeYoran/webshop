<h2>shift</h2>
<p><strong>Wie zijn wij?</strong></p>
<p>&nbsp;</p>
<p>Wij zijn Shift, een beginnende band die bestaat uit:&nbsp;</p>
<ul>
<li>Jonathan Runherd</li>
<li>Yoran Engelberts</li>
<li>Timo Engelberts
<p>&nbsp;</p>
</li>
</ul>
<p>We zijn momenteel bezig met het schrijven van ons eerste album en we hopen deze zo snel mogelijk af te hebben. Je kunt binnenkort een of twee van onze nummers op Youtube vinden, als een voorproefje op ons album.</p>
